#there is concept of skip value in string which is used to skip the value of string and print the rest of the string. It is denoted by : and the value which we want to skip is written before : and the rest of the string is printed after :
str="saloni"    
print(str[::2])  #it will skip the value of string and print the rest of the string
a="hithereitsnotawaytowriteastring"
print(a[1::3])  #it will skip the value of string and print the rest of the string"